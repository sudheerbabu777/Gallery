class Gallery extends Component {
  state = {
    activeThumbNail: imagesList[0].id,
  }

  thumbnailChange = id => {
    this.setState({activeThumbNail: id})
  }

  render() {
    const {activeThumbNail} = this.state
    const {imageUrl, imageAltText} = imagesList[activeThumbNail]
    return (
      <div className="app-container">
        <div className="bg-container">
          <img src={imageUrl} alt={imageAltText} className="image-url" />
          <h1 className="gallery-heading">Nature Photography</h1>
          <p className="gallery-description">Nature Photography by Rahul</p>
          <ul className="thumbnail-list">
            {imagesList.map(each => (
              <ThumbNailItem
                thumbNailDetails={each}
                key={each.id}
                thumbnailChange={this.thumbnailChange}
                isActive={each.id === activeThumbNail}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}

export default Gallery
